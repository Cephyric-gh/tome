/**
 * Get all accounts (hardcoded + custom)
 */
export function getAllAccounts(): Account[] {
    return [...hardcodedAccounts, ...getCustomAccounts()];
}

/**
 * Calculates the best (maximum) score for each achievement across all accounts
 */
function calculateBestScores(): number[] {
    const allAccounts = getAllAccounts();
    if (allAccounts.length === 0) return [];

    // Get the length of scores from the first account
    const scoresLength = allAccounts[0].scores.length;
    const bestScores: number[] = [];

    // For each score index
    for (let i = 0; i < scoresLength; i++) {
        let maxScore = -Infinity;

        // Find the maximum score across all accounts for this index
        for (const account of allAccounts) {
            if (account.scores[i] !== undefined) {
                maxScore = Math.max(maxScore, account.scores[i]);
            }
        }

        bestScores.push(maxScore === -Infinity ? 0 : maxScore);
    }

    return bestScores;
}

